package OOP2InheritanceInterface;

public class Car extends Vehicle {

	public void start() {
		
		System.out.println("Parent method Car----- Start");
	}
	
	public void stop() {
		System.out.println("Parent method Car ---- Stop");
	}
	public void refuel() {
		System.out.println("Parent Method Car---Refuel");
	}
}
